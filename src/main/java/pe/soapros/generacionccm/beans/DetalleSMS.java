package pe.soapros.generacionccm.beans;

public class DetalleSMS {
	String indSMS, archivo,indExito ,codError ,msgError ,valorretorno ;

	
	public String getIndSMS() {
		return indSMS;
	}

	public void setIndSMS(String indSMS) {
		this.indSMS = indSMS;
	}

	public String getArchivo() {
		return archivo;
	}

	public void setArchivo(String archivo) {
		this.archivo = archivo;
	}

	public String getIndExito() {
		return indExito;
	}

	public void setIndExito(String indExito) {
		this.indExito = indExito;
	}

	public String getCodError() {
		return codError;
	}

	public void setCodError(String codError) {
		this.codError = codError;
	}

	public String getMsgError() {
		return msgError;
	}

	public void setMsgError(String msgError) {
		this.msgError = msgError;
	}

	public String getValorretorno() {
		return valorretorno;
	}

	public void setValorretorno(String valorretorno) {
		this.valorretorno = valorretorno;
	}
	
}
